// Flat list for backward compat / search
export const CITIES = Object.entries(PROVINCES_DATA).flatMap(([prov, cities]) =>
  cities.map(c => ({ ...c, province: prov, pinyin: c.name.toLowerCase() }))
);

export function searchCity(query) {
  if (!query) return [];
  const q = query.trim();
  return CITIES.filter(c => c.name.includes(q) || c.province.includes(q)).slice(0, 8);
}

export function getCitiesByProvince(province) {
  return PROVINCES_DATA[province] || [];
}
